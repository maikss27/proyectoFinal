    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        try {
            int control = numRegTable();
            for (int i = 0; i < control; i++) { // Recorriendo Los Datos que se Encuentran en el Arraylist
                if (i < numReg) {
                    if ((((l.get(i).getProyecto_name()) != ((String) (t_proy.getValueAt(i, 1))))
                            || ((l.get(i).getProyecto_desc()) != ((String) (t_proy.getValueAt(i, 2))))
                            || ((l.get(i).getEstado()) != ((String) (t_proy.getValueAt(i, 3)))))) {
                        try {
                            ub = new proyectoBusiness();

                            ub.update(new proyecto((int) (t_proy.getValueAt(i, 0)), ((String) (t_proy.getValueAt(i, 1))),
                                    ((String) (t_proy.getValueAt(i, 2))), ((String) (t_proy.getValueAt(i, 3)))));
                        } catch (SQLException ex) {
                            Logger.getLogger(mant_proy.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                } else {
                    try {
                        ub = new proyectoBusiness();
                        ub.add(new proyecto(((String) (t_proy.getValueAt(i, 1))),
                                ((String) (t_proy.getValueAt(i, 2))), ((String) (t_proy.getValueAt(i, 3)))));
                    } catch (SQLException ex) {
                        Logger.getLogger(mant_proy.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

            try {
                ub.deleteGroup(listaDel);
            } catch (SQLException ex) {
                Logger.getLogger(mant_proy.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (StringIndexOutOfBoundsException e) {

        }
    }//GEN-LAST:event_bt_guardarActionPerformed

