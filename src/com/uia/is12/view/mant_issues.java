    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        try {
            int control = numRegTable();
            for (int i = 0; i < control; i++) { // Recorriendo Los Datos que se Encuentran en el Arraylist
                JOptionPane.showMessageDialog(null, "for");
                if (i < numReg) {
                    JOptionPane.showMessageDialog(null, "1");
                    if ((((l.get(i).getIssue_desc()) != ((String) (t_issues.getValueAt(i, 1))))
                            || ((l.get(i).getEstado()) != ((String) (t_issues.getValueAt(i, 2)))))) {
                        JOptionPane.showMessageDialog(null, "2");
                        try {
                            ub = new issuesBusiness();
                            JOptionPane.showMessageDialog(null, "update");
                            ub.update(new issues(1, ((int) (t_issues.getValueAt(i, 0))),
                                    ((String) (t_issues.getValueAt(i, 1))), 1,
                                    ((String) (t_issues.getValueAt(i, 4))),((String) (t_issues.getValueAt(i, 3)))));
                        } catch (SQLException ex) {
                            Logger.getLogger(mant_issues.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                } else {
                    try {
                        ub = new issuesBusiness();
                        ub.add(new issues(1, 1,
                                    ((String) (t_issues.getValueAt(i, 1))), 1,
                                    ((String) (t_issues.getValueAt(i, 4))),((String) (t_issues.getValueAt(i, 3)))));
                    } catch (SQLException ex) {
                        Logger.getLogger(mant_issues.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

            try {
                ub.deleteGroup("1",listaDel);
            } catch (SQLException ex) {
                Logger.getLogger(mant_issues.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (StringIndexOutOfBoundsException e) {

        }
    }//GEN-LAST:event_bt_guardarActionPerformed

    public int numRegTable() {
        int i = 0;
        try {
            while (((String) (t_issues.getValueAt(i, 1))).length() > 0) {
                i++;
            }
            return i;
        } catch (NullPointerException | IndexOutOfBoundsException e) {
            return i;
        }

    }


